package g60731.atlir4.othello.view.console;

/**
 * Represents the possible colors of tokens in the console view of the Othello game.
 */
public enum ColorTokenView {
    BLACK,      //Represents a black token
    WHITE,      //Represents a white token
    EMPTY,      // Represents an empty cell
    POSSIBLE,   // Represents a cell where a move is possible
}
